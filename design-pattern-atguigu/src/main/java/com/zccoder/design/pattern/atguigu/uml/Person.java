package com.zccoder.design.pattern.atguigu.uml;

/**
 * 个人。代码形式
 *
 * @author zc 2019-10-17
 */
public class Person {

    /**
     * ID
     */
    private Integer id;
    /**
     * 名称
     */
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
